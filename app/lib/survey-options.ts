// ตัวเลือกทั้งหมดของแบบสอบถามผลกระทบด้านสุขภาวะ
// value = ค่าที่บันทึกลง DB, label = ข้อความแสดงผล
// สำหรับข้อ AUDIT (ส่วน 3) value จะเป็น "คะแนน" 0-4 โดยตรง

export type Opt = { value: string; label: string }

// แปลง value → label สำหรับแสดงผล (เช่นในหน้าดูรายตัว); ไม่พบคืนค่า value เดิม
export function labelOf(opts: Opt[], value?: string | null): string {
  if (value === null || value === undefined || value === '') return '—'
  return opts.find((o) => o.value === value)?.label ?? value
}

